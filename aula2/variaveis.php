<h2>Variáveis</h2>
<p>São estruturas (memória)
utilizada para armazenar dados que serão processados
pelo programa.</p>

<p>Para criar uma variável em PHP basta colocar um símbolo de $ na frente de um nome e atribuir um valor para o símbolo de igual. Exemplo:
    <pre>
    $variavel=10;
    $nome="Maria";
    $nota=5.5;
</pre>
</p>

<?php
$nome="Felipe";
echo "Olá, $nome seja bem-vindo à aula de backend";
?>
